class Operations:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    @staticmethod
    def addition(a, b):
        return a + b

    @staticmethod
    def difference(a, b):
        return a - b

    @staticmethod
    def product(a, b):
        return a * b

    @staticmethod
    def quotient(a, b):
        return a / b
